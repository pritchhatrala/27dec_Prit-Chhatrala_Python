import math

num = float(input("Enter a number: "))

sqrt_result = math.sqrt(num)

ceil_result = math.ceil(num)

floor_result = math.floor(num)

print("\nResults:")
print(f" Square root of {num} is {sqrt_result}")
print(f" Ceiling value of {num} is {ceil_result}")
print(f" Floor value of {num} is {floor_result}")
